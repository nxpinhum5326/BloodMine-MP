declare(strict_types=1);

namespace pocketmine\item;

use pocketmine\block\VanillaBlocks;

class VanillaItemsIdentifier{
	/** @var Item[] */
	protected array $array = [];

	public function __construct(){
		//will be got from legacy_fixed.json (legacy item/block id list but -' replaced with true forms
		$this->array = [
			1 => VanillaBlocks::STONE()->asItem()
		];
	}

	/**
	 * @return array|Item[]
	 */
	public function getList() : array{
		return $this->array;
	}
}
